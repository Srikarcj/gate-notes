// Advanced GFG Link Generator
export class GFGLinkGenerator {
  static generateTopicUrl(branch, subject, topic) {
    const branchData = gfgLinksDatabase[branch]
    if (!branchData) return null

    const subjectData = branchData[subject]
    if (!subjectData) return null

    const topicData = subjectData.topics[topic]
    if (!topicData) return null

    return topicData
  }

  static generateSearchUrl(query) {
    return `https://www.geeksforgeeks.org/?s=${encodeURIComponent(query)}`
  }

  static generatePracticeUrl(category) {
    return `https://practice.geeksforgeeks.org/explore?category%5B%5D=${encodeURIComponent(category)}`
  }

  static generateVideoUrl(topic) {
    return `https://www.geeksforgeeks.org/videos/?s=${encodeURIComponent(topic)}`
  }

  static getAllLinksForTopic(branch, subject, topic) {
    const topicData = this.generateTopicUrl(branch, subject, topic)
    if (!topicData) return null

    return {
      tutorial: topicData.tutorial,
      practice: topicData.practice,
      problems: topicData.problems || [],
      videos: topicData.videos,
      articles: topicData.articles || [],
      search: this.generateSearchUrl(`${topic} ${subject} ${branch}`)
    }
  }
}
